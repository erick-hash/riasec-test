      Il existe de nombreux tests d'orientation ou de personnalité. La quête d'une meilleure connaissance de soi, de son
      potentiel et de ses inclinaisons est partagée par le plus grand nombre.</br>
      John Holland (1959 / 1997) s'est attaché, au moyen de l'étude statistique, à définir six traits de personnalité
      majeurs en établissant des correspondances avec les choix professionnels de centaines de personnes interrogées.</br>
      La typologie définie par Holland repose donc sur une observation préalable, contrairement à beaucoup de tests
      non professionnels qui fixent des réfèrents de manière plus ou moins arbitraire.</br>
</p>
<p>
    Un questionnaire RIASEC vise à déterminer chez un individu quels sont les traits de caractères prédominants.
    Une fois le profil établi, on peut en déduire un ensemble de métiers sollicitant les aptitudes concordantes.</br>
    Les six traits de personnalité évoqués par Holland se représentent graphiquement sous la forme d'un hexagone.
    Les six pôles de la figure géométrique ne sont pas indépendants.</br>
    Holland affirme que les distances entre ces six pôles sont inversement proportionnelles aux relations théoriques
    entre elles.</br>
